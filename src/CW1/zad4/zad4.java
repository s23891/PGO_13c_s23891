package CW1.zad4;

public class zad4 {
    public static void main(String[] args) {

        int i = 0;
        while(i < 10) {
            System.out.println( "Hello World!" );
            i++;
        }

        i = 0;
        do {
            System.out.println( "Hello World!");
            i++;
        }
        while(i < 10);

        for(i = 0; i < 10; i++) {
            System.out.println( "Hello World!");
        }
    }
}
